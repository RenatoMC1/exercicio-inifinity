#Questão 04 - Faça um programa que peça a temperatura em graus Fahrenheit,transforme e mostre a temperatura em graus Celsius.
#T (° C) = (68 ° F - 32) × 5/9 = 20 ° C

Temperatura_Fahrenheit = float(input("Digite a temperatura em ºF : "))

